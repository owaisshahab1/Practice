using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PracticeConsole.Flow_Control
{
    class BreakContinue
    {
        //Objetive:
        //Create a program to write the even numbers from 10 to 20, both included, except 16, in 3 different ways: 

        // Incrementing 2 in each step (use "continue" to skip 16)
        // Incrementing 1 in each step (use "continue")
        // With and endless loop (using "break" & "continue") 

        public static void Main33 ()
    {
        for (int i = 10; i <= 20; i += 2)
        {
            if (i == 16)
                continue;
            Console.Write (" " + i);
        }

        Console.WriteLine ();
        Console.WriteLine ();
        Console.WriteLine ();

        for (int i = 10; i <= 20; i++)
        {
            if (i % 2 == 1)
                continue;
            if (i == 16)
                continue;
            Console.Write (" " + i);
        }

        Console.WriteLine ();
        Console.WriteLine ();
        Console.WriteLine ();

        for (int i = 10; ; i += 2)
        {
            if (i == 16)
                continue;
            if (i > 20)
                break;
            Console.Write (" " + i);
        }
        Console.ReadLine ();

    }






}
}
